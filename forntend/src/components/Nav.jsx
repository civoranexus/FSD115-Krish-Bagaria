import React, { useContext, useState } from "react";
import logo from "../assets/short_logo.png";
import { IoMdSearch } from "react-icons/io";
import { CgProfile } from "react-icons/cg";
import { FaCartPlus } from "react-icons/fa";
import { HiCollection } from "react-icons/hi";
import { BiSolidContact } from "react-icons/bi";
import { FaHome } from "react-icons/fa";
import { userDataContext } from "../context/UserContext";
import { useNavigate } from "react-router-dom";
import { authDataContext } from "../context/AuthContext";
import axios from "axios";

const Nav = () => {
  let { getCurrentUser, userData } = useContext(userDataContext);
  let { serverUrl } = useContext(authDataContext);
  let [showSearch, setShowSearch] = useState(false);
  let [showProfile, setShowProfile] = useState(false);

  let navigate = useNavigate();

  const handleLogOut = async () => {
    try {
      const result = await axios.get(serverUrl + "/api/auth/logout", {
        withCredentials: true,
      });
      console.log("Logout successful:", result.data);
      getCurrentUser();
      navigate("/login");
    } catch (error) {
      console.error("Logout failed:", error);
    }
  };
  return (
    <div className="w-screen h-17.5 bg-[#ecfafaec] z-10 fixed  flex items-center justify-between px-7.5 shadow-md shadow-black">
      <div className="w-[20%] lg:w-[30%] flex items-center justify-start gap-2.5">
        <img src={logo} alt="" className="w-7.5" />
        <div>
          <h1 className="text-[25px] text-[black] font-sans">LiveStockHub</h1>
          <h1 className="text-[15px] text-[black] font-sans">
            Digital Cattle Marketplace
          </h1>
        </div>
      </div>
      <div className="w-[50%] lg:w-[40%] hidden md:flex">
        <ul className="flex items-center justify-center gap-5 text-[white]">
          <li className="text-[15px] hover:bg-slate-500 cursor-pointer bg-[#000000c9] py-2.5 px-5 rounded-2xl">
            HOME
          </li>
          <li className="text-[15px] hover:bg-slate-500 cursor-pointer bg-[#000000c9] py-2.5 px-5 rounded-2xl">
            COLLECTIONS
          </li>
          <li className="text-[15px] hover:bg-slate-500 cursor-pointer bg-[#000000c9] py-2.5 px-5 rounded-2xl">
            ABOUT
          </li>
          <li className="text-[15px] hover:bg-slate-500 cursor-pointer bg-[#000000c9] py-2.5 px-5 rounded-2xl">
            CONTACT
          </li>
        </ul>
      </div>
      <div className="w-[30%] flex items-center justify-end gap-5 ">
        <IoMdSearch
          className="w-9 h-9 text-[#000000] cursor-pointer "
          onClick={() => setShowSearch((prev) => !prev)}
        />
        {!userData && (
          <CgProfile
            className="w-7 h-7 text-[#000000] cursor-pointer "
            onClick={() => setShowProfile((prev) => !prev)}
          />
        )}
        {userData && (
          <div
            className="w-7.5 h-7.5 bg-[#080808] text-[white] rounded-full flex items-center justify-center cursor-pointer "
            onClick={() => setShowProfile((prev) => !prev)}
          >
            {userData?.user.name.slice(0, 1)}
          </div>
        )}
        <FaCartPlus className="w-8 h-8 text-[#000000] cursor-pointer hidden md:block " />

        <p className="absolute w-4.5 h-4.5 items-center justify-center bg-black px-1.25 py-1.25 text-white rounded-full text-[9px] top-2.5 right-6 hidden md:block">
          10
        </p>
      </div>
      {showSearch && (
        <div className="w-full h-20 bg-[#d8f6f9dd] absolute top-full left-0 right-0 flex items-center justify-center">
          <input
            type="text"
            className="w-[50%] h-[60%] bg-[#233533] rounded-[30px] px-12.5 placeholder:text-white text-[white] text-[18px]"
            placeholder="Search Here"
          />
        </div>
      )}
      {showProfile && (
        <div className="absolute w-55 h-37 bg-[#000000d7] top-[110%] right-[4%] border border-[#aaa9a9] rounded-[10px] z-10 ">
          <ul className="w-full h-full flex flex-col items-start justify-around  py-2.5  text-white text-[17px]">
            {!userData && (
              <li
                className="w-full hover:bg-[#2f2f2f] px-4 py-2.5 cursor-pointer"
                onClick={() => {
                  navigate("/login");
                  setShowProfile(false);
                }}
              >
                Login
              </li>
            )}
            {userData && (
              <li
                className="w-full hover:bg-[#2f2f2f] px-4 py-2.5 cursor-pointer"
                onClick={() => {
                  handleLogOut();
                  setShowProfile(false);
                }}
              >
                LogOut
              </li>
            )}
            <li className="w-full hover:bg-[#2f2f2f] px-4 py-2.5 cursor-pointer">
              Orders
            </li>
            <li className="w-full hover:bg-[#2f2f2f] px-4 py-2.5 cursor-pointer">
              About
            </li>
          </ul>
        </div>
      )}
      <div className="w-full h-22.5 flex items-center justify-between px-5 fixed bottom-0 left-0 bg-[#191818] md:hidden text-[12px]">
        <button className="text-[white] flex items-center justify-center flex-col gap-0.5">
          <FaHome className="w-7 h-7 text-[white] md:hidden" />
          Home
        </button>
        <button className="text-[white] flex items-center justify-center flex-col gap-0.5">
          <HiCollection className="w-7 h-7 text-[white] md:hidden" />
          Collection
        </button>
        <button className="text-[white] flex items-center justify-center flex-col gap-0.5">
          <BiSolidContact className="w-7 h-7 text-[white] md:hidden" />
          Contact
        </button>
        <button className="text-[white] flex items-center justify-center flex-col gap-0.5">
          <FaCartPlus className="w-7 h-7 text-[white] md:hidden" />
          Cart
        </button>
      </div>
    </div>
  );
};

export default Nav;
